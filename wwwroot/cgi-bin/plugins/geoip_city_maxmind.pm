sub RegionName($$)
{
	my $countrycode = shift || "";
	my $regioncode = shift || "";

	if ($countrycode eq "us") { return $regus{uc $regioncode} || ""; }
	if ($countrycode eq "ca") { return $regca{uc $regioncode} || ""; }

	return $regall{uc($countrycode."_".$regioncode)} || "";
}


